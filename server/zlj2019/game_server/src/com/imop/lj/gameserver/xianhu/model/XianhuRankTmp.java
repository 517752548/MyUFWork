package com.imop.lj.gameserver.xianhu.model;

public class XianhuRankTmp {
	private long roleId;
	private int count;
	private long lastTime;
	
	public XianhuRankTmp() {
		
	}
	
	public XianhuRankTmp(long roleId, int count, long lastTime) {
		this.roleId = roleId;
		this.count = count;
		this.lastTime = lastTime;
	}
	
	public long getRoleId() {
		return roleId;
	}
	
	public void setRoleId(long roleId) {
		this.roleId = roleId;
	}
	
	public int getCount() {
		return count;
	}
	
	public void setCount(int count) {
		this.count = count;
	}

	public long getLastTime() {
		return lastTime;
	}

	public void setLastTime(long lastTime) {
		this.lastTime = lastTime;
	}
	
}
