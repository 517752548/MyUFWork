package com.imop.lj.common.model;

/**
 * 属性键值信息
 * 
 * @author xiaowei.liu
 * 
 */
public class KeyValueInfo {
	private int key;
	private int value;

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
