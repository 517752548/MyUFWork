package com.imop.lj.common.model.quest;

public class QuestInfo {

	private String uuid = "";

	private int questId;

	private int destGotNum;

	private int destReqNum;

	private int questStatus;
	
	private String showRewardInfo = "";

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public int getQuestId() {
		return questId;
	}

	public void setQuestId(int questId) {
		this.questId = questId;
	}

	public int getDestGotNum() {
		return destGotNum;
	}

	public void setDestGotNum(int destGotNum) {
		this.destGotNum = destGotNum;
	}

	public int getDestReqNum() {
		return destReqNum;
	}

	public void setDestReqNum(int destReqNum) {
		this.destReqNum = destReqNum;
	}

	public int getQuestStatus() {
		return questStatus;
	}

	public void setQuestStatus(int questStatus) {
		this.questStatus = questStatus;
	}

	public String getShowRewardInfo() {
		return showRewardInfo;
	}

	public void setShowRewardInfo(String showRewardInfo) {
		this.showRewardInfo = showRewardInfo;
	}

}
