package com.imop.lj.common.model.corps;

import java.util.Arrays;

public class CorpsRedEnvelopeInfo {

	/** 红包uuid*/
	private String uuid;
	/** 所属帮派Id*/
	private long corpsId;
	/** 发红包玩家id*/
	private long sendId;
	/** 发红包玩家名称*/
	private String sendName;
	/** 红包内容*/
	private String content;
	/** 红包类型*/
	private int redEnvelopeType;
	/** 红包状态 */
	private int redEnvelopeStatus;
	/** 发送时间*/
	private long createTime;
	/** 红包总金额*/
	private int bonusAmount;
	/** 剩余红包数量*/
	private int remainingNum;
	/** 剩余红包金额*/
	private int remainingBonus;
	/** 抢红包的玩家信息*/
	private OpenRedEnveloperInfo[] openRedEnveloperInfos;
	
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public long getCorpsId() {
		return corpsId;
	}
	public void setCorpsId(long corpsId) {
		this.corpsId = corpsId;
	}
	public long getSendId() {
		return sendId;
	}
	public void setSendId(long sendId) {
		this.sendId = sendId;
	}
	public String getSendName() {
		return sendName;
	}
	public void setSendName(String sendName) {
		this.sendName = sendName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getRedEnvelopeType() {
		return redEnvelopeType;
	}
	public void setRedEnvelopeType(int redEnvelopeType) {
		this.redEnvelopeType = redEnvelopeType;
	}
	public int getRedEnvelopeStatus() {
		return redEnvelopeStatus;
	}
	public void setRedEnvelopeStatus(int redEnvelopeStatus) {
		this.redEnvelopeStatus = redEnvelopeStatus;
	}
	public long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}
	public int getBonusAmount() {
		return bonusAmount;
	}
	public void setBonusAmount(int bonusAmount) {
		this.bonusAmount = bonusAmount;
	}
	public int getRemainingNum() {
		return remainingNum;
	}
	public void setRemainingNum(int remainingNum) {
		this.remainingNum = remainingNum;
	}
	public int getRemainingBonus() {
		return remainingBonus;
	}
	public void setRemainingBonus(int remainingBonus) {
		this.remainingBonus = remainingBonus;
	}
	public OpenRedEnveloperInfo[] getOpenRedEnveloperInfoList() {
		return openRedEnveloperInfos;
	}
	public void setOpenRedEnveloperInfoList(OpenRedEnveloperInfo[] openRedEnveloperInfos) {
		this.openRedEnveloperInfos = openRedEnveloperInfos;
	}
	@Override
	public String toString() {
		return "CorpsRedEnvelopeInfo [uuid=" + uuid + ", corpsId=" + corpsId + ", sendId=" + sendId + ", sendName="
				+ sendName + ", content=" + content + ", redEnvelopeType=" + redEnvelopeType + ", redEnvelopeStatus="
				+ redEnvelopeStatus + ", createTime=" + createTime + ", bonusAmount=" + bonusAmount + ", remainingNum="
				+ remainingNum + ", remainingBonus=" + remainingBonus + ", openRedEnveloperInfos="
				+ Arrays.toString(openRedEnveloperInfos) + "]";
	}

	
}
