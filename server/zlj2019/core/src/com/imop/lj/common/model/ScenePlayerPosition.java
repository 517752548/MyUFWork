package com.imop.lj.common.model;

import java.awt.Point;
public class ScenePlayerPosition {
	private Point point;
	/** 是否瞬移 */
	private boolean instantFlag; 
	
	
	public ScenePlayerPosition(Point point, boolean instantFlag) {
		this.point = point;
		this.instantFlag = instantFlag;
	}
	
	public ScenePlayerPosition(Point point) {
		this.point = point;
	}

	public Point getPoint() {
		return point;
	}

	public void setPoint(Point point) {
		this.point = point;
	}

	public boolean isInstantFlag() {
		return instantFlag;
	}

	public void setInstantFlag(boolean instantFlag) {
		this.instantFlag = instantFlag;
	}

	@Override
	public String toString() {
		return "ScenePlayerPosition [point=" + point + ", instantFlag="
				+ instantFlag + "]";
	}
	
}
