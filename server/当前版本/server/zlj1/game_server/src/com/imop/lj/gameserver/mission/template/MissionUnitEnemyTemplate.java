package com.imop.lj.gameserver.mission.template;

import com.imop.lj.core.annotation.ExcelRowBinding;
import com.imop.lj.core.template.BeanFieldNumber;

/**
 * 关卡每个敌人的数据
 * @author Administrator
 *
 */
@ExcelRowBinding
public class MissionUnitEnemyTemplate {
	/** 敌人Id */
	@BeanFieldNumber(number = 1)
	private int enemyId;
	/** 敌人是否会主动攻击 */
	@BeanFieldNumber(number = 2)
	private int isInitiatives;
	/**
	 * @return the enemyId
	 */
	public int getEnemyId() {
		return enemyId;
	}
	/**
	 * @param enemyId the enemyId to set
	 */
	public void setEnemyId(int enemyId) {
		this.enemyId = enemyId;
	}
	/**
	 * @return the isInitiatives
	 */
	public int getIsInitiatives() {
		return isInitiatives;
	}
	/**
	 * @param isInitiatives the isInitiatives to set
	 */
	public void setIsInitiatives(int isInitiatives) {
		this.isInitiatives = isInitiatives;
	}
}
