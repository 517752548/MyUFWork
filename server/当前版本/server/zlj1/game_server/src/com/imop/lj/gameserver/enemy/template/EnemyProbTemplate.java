package com.imop.lj.gameserver.enemy.template;

import com.imop.lj.core.annotation.ExcelRowBinding;
import com.imop.lj.core.template.BeanFieldNumber;

@ExcelRowBinding
public class EnemyProbTemplate {
	/**怪物ID*/
	@BeanFieldNumber(number = 1)
	private Integer enemyId;
	
	/**怪物出现权重*/
	@BeanFieldNumber(number = 2)
	private Integer enemyProb;

	public Integer getEnemyId() {
		return enemyId;
	}

	public void setEnemyId(Integer enemyId) {
		this.enemyId = enemyId;
	}

	public Integer getEnemyProb() {
		return enemyProb;
	}

	public void setEnemyProb(Integer enemyProb) {
		this.enemyProb = enemyProb;
	}

	
	
	
}
