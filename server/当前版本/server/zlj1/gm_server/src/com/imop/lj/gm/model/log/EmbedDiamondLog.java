package com.imop.lj.gm.model.log;

import java.util.List;

public class EmbedDiamondLog extends BaseLog {

    private String wearerName;
    private int templateId;
    private int diamondIndex;
    private String diamondName;
    private String firstDiamondPropertiy;
    private String secondDiamondProperty;
    private String thirdDiamondProperty;
	public String getWearerName() {
		return wearerName;
	}
	public void setWearerName(String wearerName) {
		this.wearerName = wearerName;
	}
	public int getTemplateId() {
		return templateId;
	}
	public void setTemplateId(int templateId) {
		this.templateId = templateId;
	}
	public int getDiamondIndex() {
		return diamondIndex;
	}
	public void setDiamondIndex(int diamondIndex) {
		this.diamondIndex = diamondIndex;
	}
	public String getDiamondName() {
		return diamondName;
	}
	public void setDiamondName(String diamondName) {
		this.diamondName = diamondName;
	}
	public String getFirstDiamondPropertiy() {
		return firstDiamondPropertiy;
	}
	public void setFirstDiamondPropertiy(String firstDiamondPropertiy) {
		this.firstDiamondPropertiy = firstDiamondPropertiy;
	}
	public String getSecondDiamondProperty() {
		return secondDiamondProperty;
	}
	public void setSecondDiamondProperty(String secondDiamondProperty) {
		this.secondDiamondProperty = secondDiamondProperty;
	}
	public String getThirdDiamondProperty() {
		return thirdDiamondProperty;
	}
	public void setThirdDiamondProperty(String thirdDiamondProperty) {
		this.thirdDiamondProperty = thirdDiamondProperty;
	}

	@SuppressWarnings("unchecked")
	@Override
	public List toList(){
		List list = super.toList();
		list.add(this.wearerName);
		list.add(this.templateId);
		list.add(this.diamondIndex);
		list.add(this.firstDiamondPropertiy);
		list.add(this.secondDiamondProperty);
		list.add(this.thirdDiamondProperty);
		return list;
	}
}
