package com.imop.lj.gm.web.activity.data;

import java.sql.Timestamp;

public class ActivityGivePrizeEntity {
	/** id */
	private long id;
	//开始时间
	private Timestamp givePrizeTime;
	//给奖标示
	private int giveOrNot;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public Timestamp getGivePrizeTime() {
		return givePrizeTime;
	}
	public void setGivePrizeTime(Timestamp givePrizeTime) {
		this.givePrizeTime = givePrizeTime;
	}
	public int getGiveOrNot() {
		return giveOrNot;
	}
	public void setGiveOrNot(int giveOrNot) {
		this.giveOrNot = giveOrNot;
	}
	
}
