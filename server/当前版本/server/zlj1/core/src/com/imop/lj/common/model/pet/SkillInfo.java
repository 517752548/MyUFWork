package com.imop.lj.common.model.pet;

/**
 * 技能信息
 * 
 */
public class SkillInfo {
	private int skillId;
	private int level;
	private int skillCost;
	
	/** 技能镶嵌的效果列表 */
	private SkillEffectInfo[] embedSkillEffectList = new SkillEffectInfo[0];
	
	public int getSkillId() {
		return skillId;
	}
	public void setSkillId(int skillId) {
		this.skillId = skillId;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getSkillCost() {
		return skillCost;
	}
	public void setSkillCost(int skillCost) {
		this.skillCost = skillCost;
	}
	public SkillEffectInfo[] getEmbedSkillEffectList() {
		return embedSkillEffectList;
	}
	public void setEmbedSkillEffectList(SkillEffectInfo[] embedSkillEffectList) {
		this.embedSkillEffectList = embedSkillEffectList;
	}
	
	
}
