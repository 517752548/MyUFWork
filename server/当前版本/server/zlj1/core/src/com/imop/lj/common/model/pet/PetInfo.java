package com.imop.lj.common.model.pet;

import java.util.Arrays;

/**
 * 已招募武将基本信息
 * 
 */
public class PetInfo {
	/** 武将ID */
	private long petId;
	/** 模版ID */
	private int tplId;
	/** 颜色 */
	private int colorId;
	/** 星级 */
	private int star;
	/** 等级 */
	private int level;
	/** 经验 */
	private long exp;
	/** 武将类型 */
	private int petType;

	/** 技能列表 */
	private SkillInfo[] skillList = new SkillInfo[0];
	
	/** 一级属性附加值 */
	private int[] aPropAddArr = new int[0];
	
	/** 装备位星级 */
	private int[] aEquipStar = new int[0];
	
	/** 宠物培养增加属性 */
	private int[] trainPropArr = new int[0];
	/** 宠物培养临时属性 */
	private int[] trainTmpPropArr = new int[0];
	/** 宠物培养上限值 */
	private int trainMax = 0;
	/**宠物评分 */
	private int petScore = 0;

	public long getPetId() {
		return petId;
	}

	public void setPetId(long petId) {
		this.petId = petId;
	}

	public int getTplId() {
		return tplId;
	}

	public void setTplId(int tplId) {
		this.tplId = tplId;
	}

	public int getColorId() {
		return colorId;
	}

	public void setColorId(int colorId) {
		this.colorId = colorId;
	}

	public int getStar() {
		return star;
	}

	public void setStar(int star) {
		this.star = star;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public long getExp() {
		return exp;
	}

	public void setExp(long exp) {
		this.exp = exp;
	}

	public SkillInfo[] getSkillList() {
		return skillList;
	}

	public void setSkillList(SkillInfo[] skillList) {
		this.skillList = skillList;
	}

	public int getPetType() {
		return petType;
	}

	public void setPetType(int petType) {
		this.petType = petType;
	}

	public int[] getAPropAddArr() {
		return aPropAddArr;
	}

	public void setAPropAddArr(int[] aPropAddArr) {
		this.aPropAddArr = aPropAddArr;
	}

	public int[] getAEquipStar() {
		return aEquipStar;
	}

	public void setAEquipStar(int[] aEquipStar) {
		this.aEquipStar = aEquipStar;
	}

	public int[] getTrainPropArr() {
		return trainPropArr;
	}

	public void setTrainPropArr(int[] trainPropArr) {
		this.trainPropArr = trainPropArr;
	}

	public int[] getTrainTmpPropArr() {
		return trainTmpPropArr;
	}

	public void setTrainTmpPropArr(int[] trainTmpPropArr) {
		this.trainTmpPropArr = trainTmpPropArr;
	}

	public int getTrainMax() {
		return trainMax;
	}

	public void setTrainMax(int trainMax) {
		this.trainMax = trainMax;
	}

	public int getPetScore() {
		return petScore;
	}

	public void setPetScore(int petScore) {
		this.petScore = petScore;
	}

	@Override
	public String toString() {
		return "PetInfo [petId=" + petId + ", tplId=" + tplId + ", colorId=" + colorId + ", star=" + star + ", level="
				+ level + ", exp=" + exp + ", petType=" + petType + ", skillList=" + Arrays.toString(skillList)
				+ ", aPropAddArr=" + Arrays.toString(aPropAddArr) + ", aEquipStar=" + Arrays.toString(aEquipStar)
				+ ", trainPropArr=" + Arrays.toString(trainPropArr) + ", trainTmpPropArr="
				+ Arrays.toString(trainTmpPropArr) + ", trainMax=" + trainMax + ", petScore=" + petScore + "]";
	}
}
