package com.imop.lj.common.model.arena;

/**
 * 竞技场成员信息
 * @author yu.zhao
 *
 */
public class ArenaMemberInfo {
	private long memberId;
	private int rank;
	private String name = "";
	private int level;
	private int tplId;
	private int fightPower;
	private int isSelf;
	private int isRobot;
	private long corpsId;
	private String corpsName = "";
	
	public ArenaMemberInfo() {
		
	}

	public long getMemberId() {
		return memberId;
	}

	public void setMemberId(long memberId) {
		this.memberId = memberId;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getTplId() {
		return tplId;
	}

	public void setTplId(int tplId) {
		this.tplId = tplId;
	}

	public int getFightPower() {
		return fightPower;
	}

	public void setFightPower(int fightPower) {
		this.fightPower = fightPower;
	}

	public int getIsSelf() {
		return isSelf;
	}

	public void setIsSelf(int isSelf) {
		this.isSelf = isSelf;
	}

	public int getIsRobot() {
		return isRobot;
	}

	public void setIsRobot(int isRobot) {
		this.isRobot = isRobot;
	}

	public long getCorpsId() {
		return corpsId;
	}

	public void setCorpsId(long corpsId) {
		this.corpsId = corpsId;
	}

	public String getCorpsName() {
		return corpsName;
	}

	public void setCorpsName(String corpsName) {
		this.corpsName = corpsName;
	}

}
