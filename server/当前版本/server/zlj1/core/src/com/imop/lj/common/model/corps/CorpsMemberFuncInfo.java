package com.imop.lj.common.model.corps;

/**
 * 军团成员功能信息
 * 
 * @author xiaowei.liu
 * 
 */
public class CorpsMemberFuncInfo {
	/** 功能标题 */
	private String title;
	/**描述*/
	private String desc;
	/** 功能类型 Id */
	private int funcId;
	/** 成员id，主要是此功能参数参数 */
	private long memUUID;
	/** 是否可用 */
	private int available;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getFuncId() {
		return funcId;
	}

	public void setFuncId(int funcId) {
		this.funcId = funcId;
	}

	public long getMemUUID() {
		return memUUID;
	}

	public void setMemUUID(long memUUID) {
		this.memUUID = memUUID;
	}

	public int getAvailable() {
		return available;
	}

	public void setAvailable(int available) {
		this.available = available;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
