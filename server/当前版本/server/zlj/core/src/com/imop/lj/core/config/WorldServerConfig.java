package com.imop.lj.core.config;

import com.imop.lj.common.constants.SharedConstants;

public class WorldServerConfig {
	/**
	 * 跨服服务器ip地址
	 */
	protected String ip;
	/**
	 * 跨服服务器端口号
	 */
	protected int port;
	/**
	 * 跨服服务器功能开关true表示需要连接跨服服务器false不需要连接跨服服务器
	 */
	protected boolean turnOn = false;
	/**
	 * 此服是不是跨服服务器0代表游戏服务器1代表跨服服务器
	 */
	protected int serverType = SharedConstants.GameServer_type;

	/**
	 * 跨服服务器ip地址
	 */
	public String getIp() {
		return ip;
	}

	/**
	 * 跨服服务器ip地址
	 */
	public void setIp(String ip) {
		this.ip = ip;
	}

	/**
	 * 跨服服务器端口号
	 */
	public int getPort() {
		return port;
	}

	/**
	 * 跨服服务器端口号
	 */
	public void setPort(int port) {
		this.port = port;
	}

	/**
	 * 跨服服务器功能开关true表示需要连接跨服服务器false不需要连接跨服服务器
	 */
	public boolean isTurnOn() {
		return turnOn;
	}

	/**
	 * 跨服服务器功能开关true表示需要连接跨服服务器false不需要连接跨服服务器
	 */
	public void setTurnOn(boolean turnOn) {
		this.turnOn = turnOn;
	}

	/**
	 * 此服是不是跨服服务器0代表游戏服务器1代表跨服服务器
	 */
	public int getServerType() {
		return serverType;
	}

	/**
	 * 此服是不是跨服服务器0代表游戏服务器1代表跨服服务器
	 */
	public void setServerType(int serverType) {
		this.serverType = serverType;
	}
}
