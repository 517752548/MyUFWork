package com.imop.lj.common.model.corps;

import java.util.Arrays;

/**
 * 军团成员信息
 * 
 * @author xiaowei.liu
 * 
 */
public class CorpsMemberInfo {
	/** 成员ID */
	private long memId;
	/** 名称 */
	private String name;
	/** 模板Id */
	private int tplId;
	/** 级别 */
	private int level;
	/** 职位 */
	private int memJob;
	/** 职业 */
	private int petJob;
	/** 今日捐献 */
	private long todayDonate;
	/** 总捐献 */
	private long totalDonate;
	/** 本周贡献 */
	private long weekContribution;
	/** 总贡献 */
	private long totalContribution;
	/** 在线描述 */
	private String onlineDesc;
	/** 最后一次离线时间 */
	private long lastOfflineTime;

	/** 成员功能列表 */
	private CorpsMemberFuncInfo[] corpsMemberFuncInfoList;

	public CorpsMemberInfo() {
		this.corpsMemberFuncInfoList = new CorpsMemberFuncInfo[0];
	}

	public long getMemId() {
		return memId;
	}

	public void setMemId(long memId) {
		this.memId = memId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getMemJob() {
		return memJob;
	}

	public void setMemJob(int memJob) {
		this.memJob = memJob;
	}

	public int getPetJob() {
		return petJob;
	}

	public void setPetJob(int petJob) {
		this.petJob = petJob;
	}

	public long getTodayDonate() {
		return todayDonate;
	}

	public void setTodayDonate(long todayDonate) {
		this.todayDonate = todayDonate;
	}

	public long getTotalDonate() {
		return totalDonate;
	}

	public void setTotalDonate(long totalDonate) {
		this.totalDonate = totalDonate;
	}

	public String getOnlineDesc() {
		return onlineDesc;
	}

	public void setOnlineDesc(String onlineDesc) {
		this.onlineDesc = onlineDesc;
	}

	public CorpsMemberFuncInfo[] getCorpsMemberFuncInfoList() {
		return corpsMemberFuncInfoList;
	}

	public void setCorpsMemberFuncInfoList(
			CorpsMemberFuncInfo[] corpsMemberFuncInfoList) {
		this.corpsMemberFuncInfoList = corpsMemberFuncInfoList;
	}

	public long getWeekContribution() {
		return weekContribution;
	}

	public void setWeekContribution(long weekContribution) {
		this.weekContribution = weekContribution;
	}

	public long getTotalContribution() {
		return totalContribution;
	}

	public void setTotalContribution(long totalContribution) {
		this.totalContribution = totalContribution;
	}

	public long getLastOfflineTime() {
		return lastOfflineTime;
	}

	public void setLastOfflineTime(long lastOfflineTime) {
		this.lastOfflineTime = lastOfflineTime;
	}

	public int getTplId() {
		return tplId;
	}

	public void setTplId(int tplId) {
		this.tplId = tplId;
	}

	@Override
	public String toString() {
		return "CorpsMemberInfo [memId=" + memId + ", name=" + name + ", tplId=" + tplId + ", level=" + level
				+ ", memJob=" + memJob + ", petJob=" + petJob + ", todayDonate=" + todayDonate + ", totalDonate="
				+ totalDonate + ", weekContribution=" + weekContribution + ", totalContribution=" + totalContribution
				+ ", onlineDesc=" + onlineDesc + ", lastOfflineTime=" + lastOfflineTime + ", corpsMemberFuncInfoList="
				+ Arrays.toString(corpsMemberFuncInfoList) + "]";
	}

}
