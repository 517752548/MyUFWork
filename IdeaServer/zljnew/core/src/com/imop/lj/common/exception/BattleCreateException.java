package com.imop.lj.common.exception;

/**
 * 战斗创建失败异常
 * 
 * @author xiaowei.liu
 * 
 */
public class BattleCreateException extends Exception {
	private static final long serialVersionUID = 1L;

	public BattleCreateException() {
		super();
	}

	public BattleCreateException(String errMsg) {
		super(errMsg);
	}

	public BattleCreateException(String errMsg, Exception e) {
		super(errMsg, e);
	}
}
