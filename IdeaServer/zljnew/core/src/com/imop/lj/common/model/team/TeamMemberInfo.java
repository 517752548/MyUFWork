package com.imop.lj.common.model.team;

public class TeamMemberInfo {
	/** 队员唯一Id */
	private long uuid;
	/** 是否队长 */
	private int isLeader;
	/** 模板Id */
	private int tplId;
	/** 是否伙伴，0否，1是 */
	private int isFriend;
	/** 名字 */
	private String name;
	/** 职业 */
	private int jobTypeId;
	/** 等级 */
	private int level;
	/** 位置Id，从1开始计数 */
	private int position;
	/** 队员状态 */
	private int status;
	//玩家武器模板Id
	private int equipWeaponId;
	
	public long getUuid() {
		return uuid;
	}
	public void setUuid(long uuid) {
		this.uuid = uuid;
	}
	public int getIsLeader() {
		return isLeader;
	}
	public void setIsLeader(int isLeader) {
		this.isLeader = isLeader;
	}
	
	public int getTplId() {
		return tplId;
	}
	public void setTplId(int tplId) {
		this.tplId = tplId;
	}
	public int getIsFriend() {
		return isFriend;
	}
	public void setIsFriend(int isFriend) {
		this.isFriend = isFriend;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getJobTypeId() {
		return jobTypeId;
	}
	public void setJobTypeId(int jobTypeId) {
		this.jobTypeId = jobTypeId;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getPosition() {
		return position;
	}
	public void setPosition(int position) {
		this.position = position;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getEquipWeaponId() {
		return equipWeaponId;
	}
	public void setEquipWeaponId(int equipWeaponId) {
		this.equipWeaponId = equipWeaponId;
	}
	
}
