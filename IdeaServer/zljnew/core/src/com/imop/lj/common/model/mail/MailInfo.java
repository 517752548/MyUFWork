package com.imop.lj.common.model.mail;

import com.imop.lj.common.model.reward.RewardInfo;

public class MailInfo {

	private String uuid;

	private String title;

	private String content;

	private int mailType;

	private int mailStatus;

	private String createTime;
	
	private String deleteTime;

	private long updateTime;

	private String senderName;
	
	private int attachmented;
	
	private String recName;

	private RewardInfo rewardInfo;
	
	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getMailType() {
		return mailType;
	}

	public void setMailType(int mailType) {
		this.mailType = mailType;
	}

	public int getMailStatus() {
		return mailStatus;
	}

	public void setMailStatus(int mailStatus) {
		this.mailStatus = mailStatus;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getDeleteTime() {
		return deleteTime;
	}

	public void setDeleteTime(String deleteTime) {
		this.deleteTime = deleteTime;
	}

	public long getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(long updateTime) {
		this.updateTime = updateTime;
	}

	public String getSenderName() {
		return senderName;
	}

	public void setSenderName(String senderName) {
		this.senderName = senderName;
	}
	
	public int getAttachmented() {
		return attachmented;
	}

	public void setAttachmented(int attachmented) {
		this.attachmented = attachmented;
	}

	public String getRecName() {
		return recName;
	}

	public void setRecName(String recName) {
		this.recName = recName;
	}

	public RewardInfo getRewardInfo() {
		return rewardInfo;
	}

	public void setRewardInfo(RewardInfo rewardInfo) {
		this.rewardInfo = rewardInfo;
	}

	@Override
	public String toString() {
		return "MailInfo [uuid=" + uuid + ", title=" + title + ", content="
				+ content + ", mailType=" + mailType + ", mailStatus="
				+ mailStatus + ", createTime=" + createTime + ", deleteTime="
				+ deleteTime + ", updateTime=" + updateTime + ", senderName="
				+ senderName + ", attachmented=" + attachmented + "]";
	}

}
