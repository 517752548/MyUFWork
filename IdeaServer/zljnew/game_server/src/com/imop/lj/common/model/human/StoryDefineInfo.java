package com.imop.lj.common.model.human;

public class StoryDefineInfo {
	private int storyId;
	private int triggerTypeId;
	private String param;
	
	public StoryDefineInfo() {
		
	}
	
	public int getStoryId() {
		return storyId;
	}
	
	public void setStoryId(int storyId) {
		this.storyId = storyId;
	}
	
	public int getTriggerTypeId() {
		return triggerTypeId;
	}
	
	public void setTriggerTypeId(int triggerTypeId) {
		this.triggerTypeId = triggerTypeId;
	}
	
	public String getParam() {
		return param;
	}
	
	public void setParam(String param) {
		this.param = param;
	}

	@Override
	public String toString() {
		return "StoryDefineInfo [storyId=" + storyId + ", triggerTypeId="
				+ triggerTypeId + ", param=" + param + "]";
	}
	
}
