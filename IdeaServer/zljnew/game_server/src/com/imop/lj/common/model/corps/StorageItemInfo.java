package com.imop.lj.common.model.corps;

/**
 * 军团物品信息
 * 
 * @author xiaowei.liu
 * 
 */
public class StorageItemInfo {
	private int tempId;
	private int index;
	private int num;

	public int getTempId() {
		return tempId;
	}

	public void setTempId(int tempId) {
		this.tempId = tempId;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	@Override
	public String toString() {
		return "【tempId = " + tempId + "|index = " + index + "|num = " + num + "】";
	}
}
