package com.imop.lj.common.model.corps;

/**
 * 军团功能信息
 * 
 * @author xiaowei.liu
 * 
 */
public class CorpsFuncInfo {
	/** 功能标题 */
	private String title;
	/** 描述 */
	private String desc;
	/** 功能类型 Id */
	private int funcId;
	/** 军团id，主要是此功能参数参数 */
	private long corpsUUID;
	/** 是否可用 */
	private int available;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getFuncId() {
		return funcId;
	}

	public void setFuncId(int funcId) {
		this.funcId = funcId;
	}

	public long getCorpsUUID() {
		return corpsUUID;
	}

	public void setCorpsUUID(long corpsUUID) {
		this.corpsUUID = corpsUUID;
	}

	public int getAvailable() {
		return available;
	}

	public void setAvailable(int available) {
		this.available = available;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

}
