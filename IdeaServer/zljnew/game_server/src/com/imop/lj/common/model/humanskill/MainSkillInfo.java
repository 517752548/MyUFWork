package com.imop.lj.common.model.humanskill;

public class MainSkillInfo {

	/** 心法Id*/
	private int mindId;
	/** 心法等级*/
	private int mindLevel;
	public int getMindId() {
		return mindId;
	}
	public void setMindId(int mindId) {
		this.mindId = mindId;
	}
	public int getMindLevel() {
		return mindLevel;
	}
	public void setMindLevel(int mindLevel) {
		this.mindLevel = mindLevel;
	}
	@Override
	public String toString() {
		return "MainSkillInfo [mindId=" + mindId + ", mindLevel=" + mindLevel + "]";
	}
	
	
}
