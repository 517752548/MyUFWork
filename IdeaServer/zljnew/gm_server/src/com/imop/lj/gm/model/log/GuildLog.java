package com.imop.lj.gm.model.log;

import java.util.List;

public class GuildLog extends BaseLog {
    private String guildId;
    private String guildName;
    private int guildLevel;
    private int guildSymbolLevel;
    private int memberNums;
    private int status;



	public String getGuildId() {
		return guildId;
	}



	public void setGuildId(String guildId) {
		this.guildId = guildId;
	}



	public String getGuildName() {
		return guildName;
	}



	public void setGuildName(String guildName) {
		this.guildName = guildName;
	}



	public int getGuildLevel() {
		return guildLevel;
	}



	public void setGuildLevel(int guildLevel) {
		this.guildLevel = guildLevel;
	}



	public int getGuildSymbolLevel() {
		return guildSymbolLevel;
	}



	public void setGuildSymbolLevel(int guildSymbolLevel) {
		this.guildSymbolLevel = guildSymbolLevel;
	}



	public int getMemberNums() {
		return memberNums;
	}



	public void setMemberNums(int memberNums) {
		this.memberNums = memberNums;
	}



	public int getStatus() {
		return status;
	}



	public void setStatus(int status) {
		this.status = status;
	}



	@SuppressWarnings("unchecked")
	@Override
	public List toList() {
		List list = super.toList();
		list.add(guildId);
		list.add(guildName);
		list.add(guildLevel);
		list.add(guildSymbolLevel);
		list.add(memberNums);
		list.add(status);
		return list;
	}

}