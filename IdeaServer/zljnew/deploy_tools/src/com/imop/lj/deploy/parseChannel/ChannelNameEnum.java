public enum ChannelNameEnum {

	CN_RENREN("renren", "renren"),
	CN_QQ("qzone", "qzone"),
	CN_91("91", "91CNTemplate"),
	CN_37("37wanwan", "37wanwan"),
	;
	
	private final String name;

	private final String path;


	/**
	 * @param name 配置表中的channelName
	 * @param path	channelName对应生成文件的模板的目录名
	 */
	private ChannelNameEnum(String name, String path) {
        this.name = name;
        this.path = path;
    }


	public String getName() {
		return name;
	}


	public String getPath() {
		return path;
	}

	
}
