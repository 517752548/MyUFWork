package com.imop.lj.common.model.allocate;

public class AllocateItemInfo {
	//已被分配到的奖励道具Id
	private int itemId;
	//已被分配到的奖励道具数量
	private int num;

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	@Override
	public String toString() {
		return "AllocateItemInfo [itemId=" + itemId + ", num=" + num + "]";
	}

}
