package com.imop.lj.common.model.human;

import com.imop.lj.common.model.item.CommonItem;

public class QQChargeBuyItemInfo {
	private int chargeItemTplId;
	private String icon;
	private int buyCost;
	private int yellowVipBuyCost;
	private int giveBondNum;
	private CommonItem item;
	
	private String name;
	private String desc;
	
	public int getChargeItemTplId() {
		return chargeItemTplId;
	}
	public void setChargeItemTplId(int chargeItemTplId) {
		this.chargeItemTplId = chargeItemTplId;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public int getBuyCost() {
		return buyCost;
	}
	public void setBuyCost(int buyCost) {
		this.buyCost = buyCost;
	}
	public int getYellowVipBuyCost() {
		return yellowVipBuyCost;
	}
	public void setYellowVipBuyCost(int yellowVipBuyCost) {
		this.yellowVipBuyCost = yellowVipBuyCost;
	}
	public int getGiveBondNum() {
		return giveBondNum;
	}
	public void setGiveBondNum(int giveBondNum) {
		this.giveBondNum = giveBondNum;
	}
	public CommonItem getItem() {
		return item;
	}
	public void setItem(CommonItem item) {
		this.item = item;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
}
