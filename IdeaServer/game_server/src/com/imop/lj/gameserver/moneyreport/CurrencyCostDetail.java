package com.imop.lj.gameserver.moneyreport;

import com.imop.lj.common.LogReasons.MoneyLogReason;
import com.imop.lj.gameserver.currency.Currency;

/**
 * 金钱消耗细节
 * 
 * @author yuanbo.gao
 *
 */
public class CurrencyCostDetail {
	private MoneyLogReason reason;
	
	private Currency currency;
	
	private long totalCost;
	
	private long actualCost;

	public Currency getCurrency() {
		return currency;
	}

	public void setCurrency(Currency currency) {
		this.currency = currency;
	}

	public long getTotalCost() {
		return totalCost;
	}

	public void setTotalCost(long totalCost) {
		this.totalCost = totalCost;
	}

	public long getActualCost() {
		return actualCost;
	}

	public void setActualCost(long actualCost) {
		this.actualCost = actualCost;
	}

	public MoneyLogReason getReason() {
		return reason;
	}

	public void setReason(MoneyLogReason reason) {
		this.reason = reason;
	}
}
