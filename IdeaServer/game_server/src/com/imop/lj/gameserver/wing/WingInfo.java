package com.imop.lj.gameserver.wing;

public class WingInfo {
	private Integer templateId; //模版id
	private Integer isEquip; //是否已装备，1-是，2-否
	private Integer wingLevel; //翅膀阶数
	private Integer wingBless; //翅膀祝福值
	private Integer wingPower; //翅膀战斗力
     

	public WingInfo() {
    }

	public WingInfo(Integer templateId, Integer isEquip, Integer wingLevel, Integer wingBless, Integer wingPower) {
		super();
		this.templateId = templateId;
		this.isEquip = isEquip;
		this.wingLevel = wingLevel;
		this.wingBless = wingBless;
		this.wingPower = wingPower;
	}




	public Integer getWingBless() {
		return wingBless;
	}

	public void setWingBless(Integer wingBless) {
		this.wingBless = wingBless;
	}



	public Integer getIsEquip() {
		return isEquip;
	}


	public void setIsEquip(Integer isEquip) {
		this.isEquip = isEquip;
	}


	public Integer getTemplateId() {
		return templateId;
	}

	public void setTemplateId(Integer templateId) {
		this.templateId = templateId;
	}

	public Integer getWingLevel() {
		return wingLevel;
	}

	public void setWingLevel(Integer wingLevel) {
		this.wingLevel = wingLevel;
	}

	public Integer getWingPower() {
		return wingPower;
	}

	public void setWingPower(Integer wingPower) {
		this.wingPower = wingPower;
	}

	@Override
	public String toString() {
		return "WingInfo [templateId=" + templateId + ", isEquip=" + isEquip + ", wingLevel=" + wingLevel
				+ ", wingBless=" + wingBless + ", wingPower=" + wingPower + "]";
	}
}
