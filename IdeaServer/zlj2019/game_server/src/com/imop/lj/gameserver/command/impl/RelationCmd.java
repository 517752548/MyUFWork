/**
 * 关系命令
 * 
 */

public class RelationCmd implements IAdminCommand<ISession> {

	@Override
	public void execute(ISession playerSession, String[] commands) {
		if (!(playerSession instanceof GameClientSession)) {
			return;
		}
		Player player = ((GameClientSession) playerSession).getPlayer();
		Human human = player.getHuman();
		try {
			if (commands[0].equalsIgnoreCase("rl")) {
				Globals.getRelationService().showRecommendFriendList(human);
			} else if (commands[0].equalsIgnoreCase("usersnap")) {
				
			}
		} catch (Exception e) {
			e.printStackTrace();
			player.sendErrorMessage(e.getMessage());
		}

	}

	@Override
	public String getCommandName() {
		return CommandConstants.GM_CMD_RELATION;
	}

}
