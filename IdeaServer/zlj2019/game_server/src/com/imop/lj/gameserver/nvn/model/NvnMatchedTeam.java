package com.imop.lj.gameserver.nvn.model;

public class NvnMatchedTeam {
	//攻击方队伍id
	private int teamIdA;
	//防守方队伍id
	private int teamIdD;
	
	public NvnMatchedTeam(int teamIdA, int teamIdD) {
		this.teamIdA = teamIdA;
		this.teamIdD = teamIdD;
	}
	
	public int getTeamIdA() {
		return teamIdA;
	}
	
	public void setTeamIdA(int teamIdA) {
		this.teamIdA = teamIdA;
	}
	
	public int getTeamIdD() {
		return teamIdD;
	}
	
	public void setTeamIdD(int teamIdD) {
		this.teamIdD = teamIdD;
	}
	
}
