
public interface ICacheable
    {
        /// <summary>
        /// 缓存池名称。
        /// </summary>
        string GetPoolName();
        /// <summary>
        /// 缓存类型。
        /// </summary>
        int GetCacheType();
        /// <summary>
        /// 是否使用中。
        /// </summary>
        bool GetIsUsed();
        /// <summary>
        /// 使用。
        /// </summary>
        void Use();
        /// <summary>
        /// 闲置。
        /// </summary>
        void UnUse();
        /// <summary>
        /// 销毁。
        /// </summary>
        void Destroy();
        
        bool IsBroken();
    }