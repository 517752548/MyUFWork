using UnityEngine;
using System.Collections;
using UnityEngine.UI;

public class SimpleTipsUI : MonoBehaviour
{
    public Image tipsBg;
    public Text tipsText;
    public ContentSizeFitter textSizeFilter;

    public void Init()
    {
        tipsBg = transform.Find("Bg").GetComponent<Image>();
        tipsText = transform.Find("Text").GetComponent<Text>();
        textSizeFilter = transform.Find("Text").GetComponent<ContentSizeFitter>();
    }
}