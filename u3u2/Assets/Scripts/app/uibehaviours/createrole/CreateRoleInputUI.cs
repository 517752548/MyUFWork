using UnityEngine;
using UnityEngine.UI;

public class CreateRoleInputUI:MonoBehaviour
{
    public GameObject inputObj;
    public Image inputbg;
    public GameUUButton randomBtn;
    public GameUUButton kaishiBtn;

    public GameUUButton backBtn;

    public void Init()
    {
        inputObj = transform.gameObject;
        inputbg = transform.Find("inputbg").gameObject.GetComponent<Image>();
        randomBtn = transform.Find("randombtn").gameObject.GetComponent<GameUUButton>();
        kaishiBtn = transform.Find("kaishibtn").gameObject.GetComponent<GameUUButton>();
        backBtn = transform.Find("back/backbtn").gameObject.GetComponent<GameUUButton>();
    }
}

