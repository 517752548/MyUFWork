
namespace app.net
{
	public class FuncShowInfo
	{
		/** 功能按钮类型 */
		public int funcType;
		/** 1开启，0关闭 */
		public int isOpened;
		/** 所属功能 */
		public int ownerFuncType;
		/** 特效 */
		public int effect;
		/** 名称 */
		public string name;
		/** 描述 */
		public string desc;
		/** 数字角标，没有则为0 */
		public int showNum;
		/** 倒计时，没有则为0 */
		public long countDownTime;
		/** 按钮位置 */
		public int position;
		/** 顺序 */
		public int order;
		/** 是否首次开启，0否，1是 */
		public int isFirstOpen;
		/** 总CD时间 */
		public long totalCountDownTime;
		/** 图片ID */
		public string icon;
		/** 按钮描述 */
		public string menuDesc;
